
function redirect(place) {
  if (place == 'yt') {
    window.open('https://youtube.com/@aRandomMenno', '_blank');
  } else if (place == 'home') {
    window.location.href = 'https://aRandomMenno.github.io';
  } else if (place == 'projects') {
    window.location.href = 'https://aRandomMenno.github.io/projects';
  } else if (place == 'privacy') {
    window.location.href = 'https://aRandomMenno.github.io/privacy';
  } else if (place == 'Spijkenisse') {
    window.open('https://en.wikipedia.org/wiki/Spijkenisse', '_blank');
  } else if (place == 'Finland') {
    window.open('https://en.wikipedia.org/wiki/Finland', '_blank');
  } else if (place == 'github') {
    window.open('https://github.com/arandomMenno/', '_blank');
  } else if (place == 'discord') {
    window.open('https://discord.gg/vRpeAcKruF', '_blank')
  } else if (place == 'RiMusic') {
    window.open('https://github.com/fast4x/RiMusic', '_blank')
  } else if (place == 'ViMusic') {
    window.open('https://github.com/vfsfitvnm/ViMusic', '_blank')
  }
}
